num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))
num3 = int(input("Digite o terceiro número: "))

if num1 > (num2 + num3):
    print("O numero 1 é maior que a soma dos outros dois.")
else:
    print("O numero 1 não é maior que a soma dos outros dois.")